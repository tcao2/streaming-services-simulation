package com.model.output;


public class DisplayDemo {

    String shortName;
    String longName;
    int size;
    double current_month;
    double previous_month;
    double total;

    public String getShortName() {
        return shortName;
    }

    public void setShortName(String shortName) {
        this.shortName = shortName;
    }

    public String getLongName() {
        return longName;
    }

    public void setLongName(String longName) {
        this.longName = longName;
    }

    public int getSize() {
        return size;
    }

    public void setSize(int size) {
        this.size = size;
    }

    public double getCurrent_month() {
        return current_month;
    }

    public void setCurrent_month(double current_month) {
        this.current_month = current_month;
    }

    public double getPrevious_month() {
        return previous_month;
    }

    public void setPrevious_month(double previous_month) {
        this.previous_month = previous_month;
    }

    public double getTotal() {
        return total;
    }

    public void setTotal(double total) {
        this.total = total;
    }
}
